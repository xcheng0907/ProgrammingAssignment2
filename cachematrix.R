# Process the "vector" generated by makeCacheMatrix()
# if inverse if cached, get it;
# if not, calculate it and cache it using the 
# function in the "vector"
cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  # try to get the cached inverse
  inv <- x$getinverse()
  # if there is cached inverse
  if(!is.null(inv)){ 
    message("getting matrix inverse")
    return(inv)
  }
  # if no cached inverse;  
  # get the matrix for calculation
  data <- x$get()
  # calculate the inverse
  inv <- solve(data, ...)
  # cache the inverse
  x$setinverse(inv)
  #return inverse
  inv
}
