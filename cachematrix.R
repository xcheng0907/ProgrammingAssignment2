# This function is to create a special "vector" 
# containing a series of function to process a matrix 
makeCacheMatrix <- function(x = matrix()) {
  inv <- NULL
  # set/initialize the values of matrix and its inverse
  set <- function(y){
    x <<- y
    inv <<- NULL
  }
  # get the value of the matrix
  get <- function() x
  # set the inverse of matrix for future cache call
  setinverse <- function(inverse) inv <<- inverse
  # get the cached inverse, if any 
  getinverse <- function() inv
  # a list of function object in the environment
  list(set = set, get = get, setinverse = setinverse, getinverse = getinverse)
}


